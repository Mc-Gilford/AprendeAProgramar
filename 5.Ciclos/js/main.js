var i=0;
    // Uso con FOR
    console.log("For con variable creada\n");
    for (i=1;i<=10;i++){
        console.log(i+" ");
    }
    console.log("\n");
    console.log("For con creacion de variable\n");
    for(var j=1;j<=10;j++){
        console.log(j+" ");
    }
    console.log("\n");
    console.log("While\n");
    i=1;
    while(i<=10){
        console.log(i+" ");
        i++;
    }
    console.log("\n");
    
    console.log("Do While\n");
    i = 1;
    do{
        console.log(i+" ");
        i++;
    }while(i<=10);
    console.log("\n");